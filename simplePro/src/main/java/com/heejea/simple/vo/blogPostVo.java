package com.heejea.simple.vo;

public class blogPostVo {
	private String postId;
	private String postMainTitle;
	private String postSnTitle;
	private String postSn;
	private String postUrl;
	private String imgTitle;
	
	public String getPostId() {
		return postId;
	}
	public void setPostId(String postId) {
		this.postId = postId;
	}
	public String getPostMainTitle() {
		return postMainTitle;
	}
	public void setPostMainTitle(String postMainTitle) {
		this.postMainTitle = postMainTitle;
	}

	public String getPostSnTitle() {
		return postSnTitle;
	}
	public void setPostSnTitle(String postSnTitle) {
		this.postSnTitle = postSnTitle;
	}
	public String getPostSn() {
		return postSn;
	}
	public void setPostSn(String postSn) {
		this.postSn = postSn;
	}
	public String getPostUrl() {
		return postUrl;
	}
	public void setPostUrl(String postUrl) {
		this.postUrl = postUrl;
	}
	public String getImgTitle() {
		return imgTitle;
	}
	public void setImgTitle(String imgTitle) {
		this.imgTitle = imgTitle;
	}
	
}
